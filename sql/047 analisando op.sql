, l.ORDEM_CONFECCAO
, sum( l.QTDE_EM_PRODUCAO_PACOTE ) em_prod
FROM PCPC_040 l
GROUP BY
  l.PERIODO_PRODUCAO
, l.ORDEM_CONFECCAO
ORDER BY
  3

