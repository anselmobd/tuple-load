UPDATE BASI_020 tt
SET tt.LOTE_FABR_PECAS=100
WHERE tt.ROWID IN
(
SELECT
  t.ROWID
FROM BASI_020 t
JOIN BASI_030 r
  ON r.NIVEL_ESTRUTURA = t.BASI030_NIVEL030
 AND r.REFERENCIA = BASI030_REFERENC
WHERE r.CONTA_ESTOQUE = 2
)
;

