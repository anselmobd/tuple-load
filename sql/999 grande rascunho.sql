  ( SELECT
      MIN( le.CODIGO_ESTAGIO ) CODIGO_ESTAGIO
    FROM PCPC_040 le
    JOIN MQOP_005 ed
      ON ed.CODIGO_ESTAGIO = le.CODIGO_ESTAGIO
